// каноничный пример работы замыкания
func main() {
	test := counter()

	fmt.Println(test()) // 0
	fmt.Println(test()) // 1
	fmt.Println(test()) // 2

}

func counter() func() int {
	count := 0
	return func() int {
		value := count
		count++
		return value
	}
}

