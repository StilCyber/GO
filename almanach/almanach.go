// пример функции с одним параметром и одним возвращаемым значением
func countLetters(num int) int {
	result := 0

	for i := 0; i <= num; i++ {
		result += i
	}

	return result
}

// вариант с созданием возвращаемой переменной при объявлении функции
func countLetters(num int) (sum int) {
	sum = 0

	for i := 0; i <= num; i++ {
		sum += i
	}

	return sum
}

